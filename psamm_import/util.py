"""Utility functions."""

import os
import errno


def mkdir_p(path):
    """Make directory path if it does not already exist."""
    try:
        os.makedirs(path)
    except OSError as e:
        if e.errno != errno.EEXIST or not os.path.isdir(path):
            raise
